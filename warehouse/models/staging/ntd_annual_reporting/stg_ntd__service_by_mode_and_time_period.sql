        PARSE_BIGNUMERIC(actual_vehicles_passenger_car_miles) AS actual_vehicles_passenger_car_miles,
        PARSE_BIGNUMERIC(actual_vehicles_passenger_car_revenue_hours) AS actual_vehicles_passenger_car_revenue_hours,
