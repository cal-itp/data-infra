        PARSE_BIGNUMERIC(ada_upt) AS ada_upt,
        {{ trim_make_empty_string_null('agency') }} AS agency,
        PARSE_BIGNUMERIC(agency_voms) AS agency_voms,
        {{ trim_make_empty_string_null('aptl_questionable') }} AS aptl_questionable,
        PARSE_BIGNUMERIC(average_passenger_trip_length_aptl_) AS average_passenger_trip_length_aptl_,
        PARSE_BIGNUMERIC(average_speed) AS average_speed,
        {{ trim_make_empty_string_null('average_speed_questionable') }} AS average_speed_questionable,
        PARSE_BIGNUMERIC(brt_non_statutory_mixed_traffic) AS brt_non_statutory_mixed_traffic,
        {{ trim_make_empty_string_null('city') }} AS city,
        PARSE_BIGNUMERIC(charter_service_hours) AS charter_service_hours,
        PARSE_BIGNUMERIC(days_of_service_operated) AS days_of_service_operated,
        PARSE_BIGNUMERIC(days_not_operated_strikes) AS days_not_operated_strikes,
        PARSE_BIGNUMERIC(days_not_operated_emergencies) AS days_not_operated_emergencies,
        {{ trim_make_empty_string_null('deadhead_hours_questionable') }} AS deadhead_hours_questionable,
        {{ trim_make_empty_string_null('deadhead_miles_questionable') }} AS deadhead_miles_questionable,
        PARSE_BIGNUMERIC(directional_route_miles) AS directional_route_miles,
        {{ trim_make_empty_string_null('directional_route_miles_questionable') }} AS directional_route_miles_questionable,
        PARSE_BIGNUMERIC(mixed_traffic_right_of_way) AS mixed_traffic_right_of_way,
        {{ trim_make_empty_string_null('mode') }} AS mode,
        {{ trim_make_empty_string_null('mode_name') }} AS mode_name,
        PARSE_BIGNUMERIC(mode_voms) AS mode_voms,
        {{ trim_make_empty_string_null('mode_voms_questionable') }} AS mode_voms_questionable,
        {{ trim_make_empty_string_null('organization_type') }} AS organization_type,
        PARSE_BIGNUMERIC(passenger_miles) AS passenger_miles,
        {{ trim_make_empty_string_null('passenger_miles_questionable') }} AS passenger_miles_questionable,
        PARSE_BIGNUMERIC(passengers_per_hour) AS passengers_per_hour,
        {{ trim_make_empty_string_null('passengers_per_hour_questionable') }} AS passengers_per_hour_questionable,
        PARSE_BIGNUMERIC(primary_uza_area_sq_miles) AS primary_uza_area_sq_miles,
        PARSE_BIGNUMERIC(primary_uza_code) AS primary_uza_code,
        {{ trim_make_empty_string_null('primary_uza_name') }} AS primary_uza_name,
        PARSE_BIGNUMERIC(primary_uza_population) AS primary_uza_population,
        PARSE_BIGNUMERIC(report_year AS INT64) AS report_year,
        {{ trim_make_empty_string_null('reporter_type') }} AS reporter_type,
        {{ trim_make_empty_string_null('scheduled_revenue_miles_questionable') }} AS scheduled_revenue_miles_questionable,
        PARSE_BIGNUMERIC(scheduled_vehicles_passenger_car_revenue_miles) AS scheduled_vehicles_passenger_car_revenue_miles,
